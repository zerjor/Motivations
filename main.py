# Функция для команды /start
def start(update: Update, context: CallbackContext) -> None:
    user = update.effective_user
    welcome_message = (
        f"Привет, {user.first_name}! 👋\n"
        "Я - бот-мотиватор! Я помогу тебе найти вдохновение и мотивацию.\n\n"
        "Вот что я могу:/n"
        "/getmotivation - получить мотивирующую фразу\n"
        "/getsmartquote - получить умную мотивирующую цитату\n\n"
        "Просто введи одну из этих команд, и я отправлю тебе что-нибудь вдохновляющее!"
    )
    update.message.reply_text(welcome_message)

# Функция для команды /help
def help_command(update: Update, context: CallbackContext) -> None:
    help_message = (
        "Я - бот-мотиватор. Доступные команды:\n\n"
        "/start - приветствие\n"
        "/getmotivation - получить мотивирующую фразу\n"
        "/getsmartquote - получить умную мотивирующую цитату\n"
        "/help - показать это сообщение"
    )
    update.message.reply_text(help_message)

# Функция для команды /getmotivation
def get_motivation(update: Update, context: CallbackContext) -> None:
    random_phrase = random.choice(MOTIVATIONAL_PHRASES)
    update.message.reply_text(random_phrase)

# Функция для команды /getsmartquote
def get_smart_quote(update: Update, context: CallbackContext) -> None:
    random_quote = random.choice(SMART_QUOTES)
    update.message.reply_text(random_quote)

# Функция для обработки любых других сообщений
def handle_other_messages(update: Update, context: CallbackContext) -> None:
    update.message.reply_text(
        "Введи /getmotivation для получения мотивирующей фразы "
        "или /getsmartquote для получения мотивирующей цитаты"
    )

